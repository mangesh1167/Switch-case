package controlflowstatements;

public class Evenorodd {

		public static void main(String[] args) {
			
			int num = 5;
			switch(num % 2) {
			  
			case 1: 
				System.out.println("Odd");
				break;
				
			case 2:
				System.out.println("Even");
				break;
				
			 
			
		}

	}
}

